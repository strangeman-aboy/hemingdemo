function setFlowRole(role) {
  const grid = document.querySelector(".brain-flow-grid");
  if (!grid) return;

  grid.dataset.activeFlow = role;
  grid.classList.add("is-focused");

  document.querySelectorAll(".flow-role-button").forEach((button) => {
    button.classList.toggle("active", button.dataset.flowRole === role);
  });

  grid.querySelectorAll("[data-flow-source]").forEach((item) => {
    const roles = item.dataset.flowSource.split(" ");
    item.classList.toggle("flow-active", roles.includes(role));
  });

  grid.querySelectorAll("[data-flow-output]").forEach((item) => {
    item.classList.toggle("flow-active", item.dataset.flowOutput === role);
  });
}

document.querySelectorAll(".flow-role-button").forEach((button) => {
  button.addEventListener("click", () => {
    setFlowRole(button.dataset.flowRole);
  });
});

