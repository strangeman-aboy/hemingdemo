function renderPlatform(view) {
  return `
    <div class="detail-topline">
      <span>${view.label}</span>
      <strong>${view.title}</strong>
    </div>
    <div class="detail-grid">
      ${view.cards.map(([label, text]) => `
        <article>
          <span>${label}</span>
          <p>${text}</p>
        </article>
      `).join("")}
    </div>
    <div class="detail-example">
      <span>示例选题 / 场景</span>
      <p>${view.example}</p>
    </div>
    <div class="case-grid" aria-label="公开案例参考">
      ${view.cases.map((item) => `
        <a class="case-card" href="${item.url}" target="_blank" rel="noreferrer">
          <span>${item.label}</span>
          <strong>${item.title}</strong>
          <p>${item.desc}</p>
        </a>
      `).join("")}
    </div>
  `;
}

